print('Dobro, Triplo e Raíz Quadrada')
num = int(input('Diga um número: '))
dobro = num * 2
triplo = num * 3
raiz = num ** (1 / 2)
print('O dobro de {} vale {}! '.format(num, dobro))
print('O triplo de {} vale {}! '.format(num, triplo))
print('A raíz quadrada de {} é igual a {:.2f}! '.format(num, raiz))

